void next_tact(void)
{
	unsigned short int instr, a, note;
	
	instr=(0xff & mus[t++]);

		if(instr==0xff)
		{
			if(loop)
			{
				t=0;
				instr=(0xff & mus[t++]);
			} else
			{
				enabled=0;
				instr=0;
				return ;
			}
			
		}
		while(instr!=0x00)
		{
			a=(0xff & mus[t++]);
			note=(0xFF & mus[t++]);
			if(a==1) sound_play(instr,0,note-12);
			if(a==0) sound_stop(instr,0,note-12);
			instr=(0xff & mus[t++]);
			if(instr==0xff)
		{
			if(loop)
			{
				t=0;
				instr=(0xff & mus[t++]);
			} else
			{
				enabled=0;
				instr=0;
				return ;
			}
			
		}
			
		}
		tt=1;
		return;
}


